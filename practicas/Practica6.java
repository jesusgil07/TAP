    private void guardarArchivo() {
        // Windows,N,N,N,8 
        String sSisOper = "Windows";
        String sProgra = "N";
        String sDiseno = "N";
        String sAdmon  = "N";
        String sHoras  = "0";
                
        if (this.jRadioButton2.isSelected()){
            sSisOper = "Linux";
        }
        
        if (this.jRadioButton3.isSelected()){
            sSisOper = "Mac";
        }
        
        if (this.jCheckBox1.isSelected()){
            sProgra = "S";
        }

        if (this.jCheckBox2.isSelected()){
            sDiseno = "S";
        }

        if (this.jCheckBox3.isSelected()){
            sAdmon = "S";
        }
        
        sHoras = Integer.toString(this.jSlider1.getValue());

        String sRespuesta = String.format("%s,%s,%s,%s,%s\n", sSisOper,sProgra,sDiseno,sAdmon,sHoras);
       
        FileWriter fw = null;
        
        try {
            fw = new FileWriter("respuestas.txt",true);
            fw.write(sRespuesta);
            fw.close(); 
        } catch (IOException ex) {
            Logger.getLogger(Practica6.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.guardarBD(sSisOper,sProgra,sDiseno,sAdmon,this.jSlider1.getValue());
               
    }

    private void guardarBD(String sSisOper, String sProgra, String sDiseno, String sAdmon, int iHoras) {
        Statement stmt;
        String sInsert;
        
        sInsert = String.format("INSERT INTO respuestas (sSisOper,cProgra,cDiseno,cAdmon,iHoras)  VALUES ('%s','%s','%s','%s',%d)",sSisOper,sProgra,sDiseno,sAdmon,iHoras);
        
        try {
            if (conn == null){
                conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/encuesta?"
                                      + "useUnicode=true&useJDBCCompliantTimezoneShift=true&useLegacyDatetimeCode=false&serverTimezone=UTC&user=encuesta&password=encuesta");
                
                stmt = conn.createStatement();
                stmt.execute(sInsert);
                conn.close();
                conn = null;
            }
            
        } catch (SQLException sqle){
            System.out.println(sqle.getMessage());
        }
        
    }
